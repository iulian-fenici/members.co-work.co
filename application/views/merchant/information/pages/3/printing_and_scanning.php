        <h3>Printing and Scanning</h3>
        <p>Next to the Kitchen you will find our print area with the Brother Printer and Scanner.</p>

        <p>If you are reading this for the first time please now download the driver for the Brother printer based on if you are a PC or Mac user. If you have any further questions please let me know. Please note that there are two different PC drivers based on if you have a 32 or 64 bit PC (i.e. if one doesn't work try the other!)</p>

        <p>Also please note that if you are installing the driver on a Mac you must plug into the network to complete this process as it won't work via the wireless. Once its installed however you will be able to print via the wireless connection providing you read through the how to guide on installing the drivers corrrectly.</p>

        <p>We provide use of the printers on communal use under our fair use policy. This means that you're more than welcome to use the printer or scanner on an ad-hoc basis but please don't use it as a full time business tool i.e. for mail shots or hundreds of presentations</p>

        <p>To scan a document you will need a memory stick.</p>

        <p>If you have any further questions or want some help on how to use the printer or scanner please refer to the user guides on the wall in the printer area or please don't hesitate to ask one of your fellow co-workers.</p>
