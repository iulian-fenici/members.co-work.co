        <h3>Phones</h3>
        <p>To set up a conference call
            1. Connect the first call either by accepting an incoming call, or by connecting an outgoing call
            2. Put the call on hold
            3. Dial the third member of the conference call
            4. When the third member has connected, press the Confirm button (the TICK button)
            5. Now all three members are connected</p>
